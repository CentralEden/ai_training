        // 推奨専門分野を決定する関数
        function getRecommendedSpecializations(topCareer, selectedCheckboxes) {
            const specializations = [];
            const selectedSpecializations = [];
            
            // 選択された専門分野項目を取得
            selectedCheckboxes.forEach(checkbox => {
                if (checkbox.dataset.category === 'specialized' || checkbox.dataset.specializationBoost) {
                    const spec = checkbox.dataset.specialization || checkbox.dataset.specializationBoost;
                    if (spec && !selectedSpecializations.includes(spec)) {
                        selectedSpecializations.push(spec);
                    }
                }
            });
            
            // 選択された専門分野がある場合はそれを優先
            if (selectedSpecializations.length > 0) {
                const specMap = {
                    'time-series': '時系列解析',
                    'computer-vision': '画像認識・CV',
                    'nlp': '自然言語処理',
                    'recommendation': 'レコメンデーション',
                    'anomaly-detection': '異常検知',
                    'data-engineering': 'データエンジニアリング'
                };
                
                selectedSpecializations.forEach(spec => {
                    if (specMap[spec]) {
                        specializations.push(specMap[spec]);
                    }
                });
            }
            
            // 選択された専門分野がない場合、キャリアに基づいて推奨
            if (specializations.length === 0) {
                switch(topCareer) {
                    case 'data-science':
                        specializations.push('時系列解析', 'レコメンデーション');
                        break;
                    case 'ml-engineer':
                        specializations.push('画像認識・CV', '自然言語処理');
                        break;
                    case 'cv-engineer':
                        specializations.push('画像認識・CV');
                        break;
                    case 'nlp-engineer':
                        specializations.push('自然言語処理');
                        break;
                    case 'data-engineer':
                        specializations.push('データエンジニアリング');
                        break;
                    case 'consultant':
                        specializations.push('時系列解析', 'レコメンデーション');
                        break;
                    default:
                        specializations.push('案件に応じて選択');
                }
            }
            
            return specializations.length > 0 ? specializations : ['案件に応じて選択'];
        }

