        // スコア計算とリアルタイム更新（専門分野重み強化版）
        function calculateScores() {
            const checkboxes = document.querySelectorAll('input[type="checkbox"]:checked');
            const scores = {};
            const specializationBoosts = {}; // 専門分野ブースト追跡
            
            // 全キャリアパスのスコアを初期化
            Object.keys(careerPaths).forEach(key => {
                scores[key] = 0;
            });
            
            // チェックされた項目に基づいてスコアを計算
            checkboxes.forEach(checkbox => {
                const weights = checkbox.dataset.weight;
                const baseWeight = parseInt(checkbox.dataset.baseWeight) || 1; // デフォルトは1
                const category = checkbox.dataset.category;
                const specialization = checkbox.dataset.specialization;
                const specializationBoost = checkbox.dataset.specializationBoost;
                
                if (weights) {
                    weights.split(',').forEach(career => {
                        if (scores[career] !== undefined) {
                            let multiplier = baseWeight;
                            
                            // 専門分野項目の場合は追加の重み付け
                            if (category === 'specialized') {
                                multiplier *= 1.5; // 専門分野項目は1.5倍
                            }
                            
                            // プロジェクト経験による専門分野強化
                            if (category === 'project' && specializationBoost) {
                                multiplier *= 1.3; // プロジェクト経験は1.3倍
                                
                                // 対応する専門分野を追跡
                                if (!specializationBoosts[specializationBoost]) {
                                    specializationBoosts[specializationBoost] = 0;
                                }
                                specializationBoosts[specializationBoost] += 2;
                            }
                            
                            scores[career] += multiplier;
                        }
                    });
                }
            });
            
            // 専門分野ブーストの適用
            Object.keys(specializationBoosts).forEach(specialization => {
                const boost = specializationBoosts[specialization];
                
                // 専門分野に対応するキャリアパスにブーストを適用
                switch(specialization) {
                    case 'time-series':
                        scores['data-science'] = (scores['data-science'] || 0) + boost;
                        scores['ml-engineer'] = (scores['ml-engineer'] || 0) + boost * 0.8;
                        break;
                    case 'computer-vision':
                        scores['cv-engineer'] = (scores['cv-engineer'] || 0) + boost;
                        scores['ml-engineer'] = (scores['ml-engineer'] || 0) + boost * 0.8;
                        break;
                    case 'nlp':
                        scores['nlp-engineer'] = (scores['nlp-engineer'] || 0) + boost;
                        scores['ml-engineer'] = (scores['ml-engineer'] || 0) + boost * 0.8;
                        break;
                    case 'recommendation':
                        scores['data-science'] = (scores['data-science'] || 0) + boost;
                        scores['ml-engineer'] = (scores['ml-engineer'] || 0) + boost * 0.8;
                        break;
                    case 'anomaly-detection':
                        scores['data-science'] = (scores['data-science'] || 0) + boost;
                        scores['ml-engineer'] = (scores['ml-engineer'] || 0) + boost * 0.8;
                        break;
                    case 'data-engineering':
                        scores['data-engineer'] = (scores['data-engineer'] || 0) + boost;
                        scores['mlops-engineer'] = (scores['mlops-engineer'] || 0) + boost * 0.8;
                        break;
                }
            });
            
            // 経験年数による重み付け
            const experience = parseInt(document.getElementById('experience').value) || 0;
            if (experience >= 8) {
                scores['ai-development-director'] += 3;
                scores['consultant'] += 2;
            }
            if (experience >= 5) {
                scores['consultant'] += 1.5;
                scores['product-manager'] += 1.5;
                scores['ai-development-director'] += 1;
                scores['ai-architect'] += 1;
            }
            if (experience >= 3) {
                scores['ml-engineer'] += 1;
                scores['data-engineer'] += 1;
                scores['mlops-engineer'] += 1;
                scores['cv-engineer'] += 1;
                scores['nlp-engineer'] += 1;
                scores['audio-engineer'] += 1;
                scores['robotics-engineer'] += 1;
            }
            if (experience >= 1) {
                scores['data-science'] += 0.5;
            }
            
            return scores;
        }

